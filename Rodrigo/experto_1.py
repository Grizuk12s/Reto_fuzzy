# =============================================================================
# BLOQUE — MOTOR DE REGLAS (COOLDOWN POR FAMILIA/SP + NO-OP)
# =============================================================================

def motor_reglas(
    result,
    REGLAS,
    tiempo_actual_s,
    cooldowns_accion,              # dict: familia -> t_exp
    alpha_min=0.05,
    alpha_cooldown=0.20,
    tnorm="min",
    ACCIONES_FISICAS=None,
):
    """
    Motor de reglas con esperas POR FAMILIA (por defecto: por SP).
    - cooldowns_accion guarda tiempos por familia: cooldowns_accion[familia] = t_exp
    - cualquier acción del mismo SP comparte cooldown.
    - acciones no-op (paso 0, ej 'mantener') NO aplican cooldown y pueden no mapear a SP.
    """

    if cooldowns_accion is None:
        cooldowns_accion = {}

    t_now = float(tiempo_actual_s)

    # ---------------------------------------------------------------------
    # 0) Limpieza: elimina cooldowns expirados (guardados por familia)
    # ---------------------------------------------------------------------
    for fam, t_exp in list(cooldowns_accion.items()):
        try:
            if t_now >= float(t_exp):
                cooldowns_accion.pop(fam, None)
        except Exception:
            cooldowns_accion.pop(fam, None)

    # ---------------------------------------------------------------------
    # 1) Mapa de pasos (ACCIONES_FISICAS) — robusto
    # ---------------------------------------------------------------------
    if ACCIONES_FISICAS is not None:
        pasos = ACCIONES_FISICAS
    else:
        pasos = globals().get("ACCIONES_FISICAS", {})

    if pasos is None:
        pasos = {}
    if not isinstance(pasos, dict):
        try:
            pasos = dict(pasos)
        except Exception:
            pasos = {}

    def _raw_paso(accion: str) -> float:
        try:
            return float(pasos.get(accion, np.nan))
        except Exception:
            return float("nan")

    def _es_noop(accion: str) -> bool:
        if accion in pasos:
            r = _raw_paso(accion)
            return np.isfinite(r) and (r == 0.0)
        return False

    def _accion_valida(accion: str) -> bool:
        # debe existir en ACCIONES_FISICAS
        if accion not in pasos:
            return False
        # no-op se acepta aunque no tenga SP
        if _es_noop(accion):
            return True
        # si no es no-op, debe mapear a SP
        return (ACCION_A_SP.get(accion) is not None)

    # ---------------------------------------------------------------------
    # 2) Construcción de candidatos
    # ---------------------------------------------------------------------
    cand = []
    for nombre_bloque, reglas in REGLAS.items():
        for regla in reglas:
            acciones = list((regla or {}).get("acciones", []) or [])

            # filtra acciones inválidas
            acciones = [a for a in acciones if _accion_valida(a)]
            if not acciones:
                continue

            # unique preserving order
            seen = set()
            acciones = [a for a in acciones if not (a in seen or seen.add(a))]

            # filtrar por cooldown (por familia)
            acciones_exec = []
            acciones_blocked = []

            for a in acciones:
                fam = _cooldown_family(a)
                t_exp = cooldowns_accion.get(fam, None)

                if t_exp is None:
                    acciones_exec.append(a)
                else:
                    try:
                        if t_now < float(t_exp):
                            acciones_blocked.append(a)
                        else:
                            acciones_exec.append(a)
                    except Exception:
                        acciones_exec.append(a)

            # si no queda nada ejecutable, descarta
            if not acciones_exec:
                continue

            w = float(fuerza_regla(result, regla, tnorm=tnorm))
            if w >= float(alpha_min):
                cand.append({
                    "prioridad": int(regla["prioridad"]),
                    "w": w,
                    "bloque": nombre_bloque,
                    "regla": regla,
                    "acciones_originales": acciones,
                    "acciones_ejecutables": acciones_exec,
                    "acciones_bloqueadas": acciones_blocked,
                })

    if not cand:
        return {
            "regla_activada": None,
            "bloque": None,
            "acciones": [],
            "acciones_originales": [],
            "acciones_bloqueadas": [],
            "descripcion": "",
            "duracion": 0,
            "duracion_regla_default": 0.0,
            "cooldown_hasta": None,
            "cooldowns_accion_hasta": {},      # acción -> t_exp (por familia)
            "duraciones_accion_s": {},         # acción -> duración aplicada (s)
            "prioridad": None,
            "alpha_max": 0.0,
            "belief": 0.0,
        }

    # ---------------------------------------------------------------------
    # 3) Selección por prioridad y w
    # ---------------------------------------------------------------------
    p_min = min(c["prioridad"] for c in cand)
    cand_p = [c for c in cand if c["prioridad"] == p_min]

    def _key_sel(c):
        rid = int(c["regla"]["id"])
        return (float(c["w"]), int(len(c["acciones_ejecutables"])), -rid)

    best = max(cand_p, key=_key_sel)

    regla_best = best["regla"]
    bloque_best = best["bloque"]
    w_best = float(best["w"])
    rid = int(regla_best["id"])

    acciones_originales = list(best["acciones_originales"])
    acciones_exec = list(best["acciones_ejecutables"])
    acciones_blocked = list(best["acciones_bloqueadas"])

    duracion_raw = (regla_best or {}).get("duracion", 0)
    dur_regla_default = 0.0
    if isinstance(duracion_raw, (int, float, np.number)):
        dur_regla_default = float(duracion_raw)

    # ---------------------------------------------------------------------
    # 4) Aplicar cooldown por familia (solo si w_best >= alpha_cooldown)
    # ---------------------------------------------------------------------
    duraciones_accion_s = {}
    if (w_best >= float(alpha_cooldown)):
        for a in acciones_exec:
            if _es_noop(a):
                continue

            dur_a = float(cooldown_segundos_para_accion(a, regla_best) or 0.0)
            duraciones_accion_s[a] = dur_a

            if dur_a > 0.0:
                fam = _cooldown_family(a)
                cooldowns_accion[fam] = float(t_now + dur_a)

    # logging: acción -> cooldown_hasta (resuelto por familia)
    cooldowns_accion_hasta = {}
    for a in acciones_originales:
        fam = _cooldown_family(a)
        cooldowns_accion_hasta[a] = cooldowns_accion.get(fam, None)

    # cooldown_hasta global: mínimo entre familias activas de acciones ejecutadas
    futuros = []
    for a in acciones_exec:
        fam = _cooldown_family(a)
        t_exp = cooldowns_accion.get(fam, None)
        if t_exp is not None:
            try:
                t_exp_f = float(t_exp)
                if t_exp_f > t_now:
                    futuros.append(t_exp_f)
            except Exception:
                pass
    cooldown_hasta = float(min(futuros)) if futuros else None

    return {
        "regla_activada": rid,
        "bloque": bloque_best,
        "acciones": acciones_exec,
        "acciones_originales": acciones_originales,
        "acciones_bloqueadas": acciones_blocked,
        "descripcion": (regla_best or {}).get("descripcion", ""),
        "duracion": duracion_raw,
        "duracion_regla_default": float(dur_regla_default),
        "cooldown_hasta": cooldown_hasta,
        "cooldowns_accion_hasta": cooldowns_accion_hasta,
        "duraciones_accion_s": duraciones_accion_s,
        "prioridad": int(regla_best["prioridad"]),
        "alpha_max": float(w_best),
        "belief": float(w_best),
    }

