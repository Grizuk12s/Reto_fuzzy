def evaluar_pendiente_var(var_name, pv, t_s, hist, dt_min_floor=1e-6):
    """
    Devuelve:
      dom ∈ {SUBIENDO, ESTABLE, BAJANDO}
      pert = {SUBIENDO, ESTABLE, BAJANDO}
      belief NO se calcula aquí; belief = fuerza_regla del motor.
    """
    modelo = PEND_MODELOS[var_name]
    prev = hist.get(var_name)

    if prev is None:
        slope_per_min = 0.0
    else:
        t_prev, pv_prev = prev
        dt_min = max((t_s - t_prev) / 60.0, dt_min_floor)
        slope_per_min = (float(pv) - float(pv_prev)) / dt_min

    dom_raw, p, pert_raw, inf = modelo.evaluar(slope_per_min)

    # normalizar
    pert = {str(k).upper(): float(v) for k, v in pert_raw.items()}
    dom = str(dom_raw).upper()

    hist[var_name] = (float(t_s), float(pv))

    return {
        "dom": dom,
        "val": float(pert.get(dom, inf)),
        "offset": float(slope_per_min),
        "pert": pert,
        "slope_per_min": float(slope_per_min),
    }

