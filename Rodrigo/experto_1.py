def _unidad_accion(accion):
    if "tonelaje" in accion:
        return "ton/h"
    if "agua" in accion:
        return "m3/h"
    if "rpm_bomba" in accion:
        return "%"
    return ""

def _modo_desde_accion(accion: str) -> str:
    """
    Inferencia desde nombre (tal como pediste):
      - aumentar_*  => inc_rap
      - disminuir_* => dec_rap
      - *_suave     => inc / dec (según signo)
      - mantener    => inc (pero delta 0)
    """
    a = str(accion).lower().strip()
    if a == "mantener":
        return "inc"

    suave = ("_suave" in a)

    if a.startswith("aumentar"):
        return "inc" if suave else "inc_rap"
    if a.startswith("disminuir"):
        return "dec" if suave else "dec_rap"
    # fallback conservador
    return "inc"

def scale_tabla(belief: float, modo: str) -> float:
    """
    Tabla solicitada (normalizada a [0,1]):
      Belief 1   => inc_rap=1.0, inc=1.0, dec=1.0, dec_rap=1.0
      Belief 0.5 => inc_rap=0.5 (25/50), inc=0.4 (10/25), dec=0.4, dec_rap=0.5
      Belief 0   => 0

    En la práctica: delta = sign * scale * max_step
    """
    b = float(np.clip(belief, 0.0, 1.0))
    m = str(modo).lower()

    # ratios en 0.5
    r05 = 0.5 if m in ("inc_rap", "dec_rap") else 0.4
    return float(np.interp(b, [0.0, 0.5, 1.0], [0.0, r05, 1.0]))

def defuzzy_sugeno(result, salida_motor, ACCIONES_FISICAS=ACCIONES_FISICAS):
    """
    Sugeno: belief = fuerza de regla (salida_motor['belief'])
    Para cada acción:
      z = sign * scale_tabla(belief, modo) * |max_step|
    Retorna lista de comandos (uno por acción).

    Cambio clave:
      - Si la acción es no-op (raw == 0), NO genera comando.
      - Esto permite que "mantener" exista como acción semántica sin SP asociado.
    """
    rid = salida_motor.get("regla_activada")
    if rid is None:
        return []

    belief = float(salida_motor.get("belief", 0.0))
    acciones = salida_motor.get("acciones", []) or []

    comandos = []
    for accion in acciones:
        if accion not in ACCIONES_FISICAS:
            continue

        raw = float(ACCIONES_FISICAS[accion])

        # no-op => no emitir comando (incluye "mantener": 0)
        if raw == 0.0:
            continue

        sp_col = ACCION_A_SP.get(accion)
        if sp_col is None:
            # si no hay SP asociado y además no es no-op (ya filtrado), es inconsistencia
            continue

        max_step = abs(raw)
        signo = 1.0 if raw > 0 else (-1.0 if raw < 0 else 0.0)

        modo = _modo_desde_accion(accion)
        sc = float(scale_tabla(belief, modo))
        delta = float(signo * sc * max_step)

        comandos.append({
            "accion": accion,
            "sp_col": sp_col,
            "unidad": _unidad_accion(accion),
            "belief": belief,
            "modo": modo,
            "scale": sc,
            "max_step": max_step,
            "delta_sp": delta
        })

    return comandos

