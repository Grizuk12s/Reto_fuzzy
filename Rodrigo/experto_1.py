def crear_clase_fuzzy_norm(nombre_clase, offset, **conjuntos):
    class FuzzyTemplate:
        def __init__(self):
            self.offset = np.array(offset, dtype=float)
            for nombre, valores in conjuntos.items():
                setattr(self, nombre, np.array(valores, dtype=float))
            for nombre in conjuntos.keys():
                vector = getattr(self, nombre)
                setattr(
                    self,
                    f"mf_{nombre}",
                    lambda x, v=vector: np.interp(x, self.offset, v, left=v[0], right=v[-1])
                )

        def calcular_offset(self, pv, lmin, lmax):
            den = (float(lmax) - float(lmin))
            if abs(den) < 1e-12:
                return 0.5
            return (float(pv) - float(lmin)) / den

        def evaluar(self, pv, lmin, lmax):
            off = self.calcular_offset(pv, lmin, lmax)
            pertenencias = {}
            for nombre in conjuntos.keys():
                mf = getattr(self, f"mf_{nombre}")
                pertenencias[nombre] = float(np.clip(mf(off), 0, 1))
            dominante = max(pertenencias, key=pertenencias.get)
            val_dom = pertenencias[dominante]
            return dominante, val_dom, off, pertenencias

    FuzzyTemplate.__name__ = nombre_clase
    return FuzzyTemplate


def crear_clase_fuzzy_pendiente(nombre_clase, x, **conjuntos):
    class FuzzyPendienteTemplate:
        def __init__(self):
            self.x = np.array(x, dtype=float)
            for nombre, valores in conjuntos.items():
                setattr(self, nombre, np.array(valores, dtype=float))
            for nombre in conjuntos.keys():
                vector = getattr(self, nombre)
                setattr(
                    self,
                    f"mf_{nombre}",
                    lambda p, v=vector: np.interp(p, self.x, v, left=v[0], right=v[-1])
                )

        def evaluar(self, pendiente):
            p = float(pendiente)
            pertenencias = {}
            for nombre in conjuntos.keys():
                mf = getattr(self, f"mf_{nombre}")
                pertenencias[nombre] = float(np.clip(mf(p), 0, 1))
            dominante = max(pertenencias, key=pertenencias.get)
            inferencia = pertenencias[dominante]
            return dominante, p, pertenencias, inferencia

    FuzzyPendienteTemplate.__name__ = nombre_clase
    return FuzzyPendienteTemplate


def crear_clase_fuzzy_varianza(nombre_clase, x, **conjuntos):
    """
    Template fuzzy para varianza (σ²).

    Parámetros
    ----------
    nombre_clase : str
        Nombre de la clase generada.
    x : list/array
        Eje de varianza (σ²) donde defines las MFs. Debe ser creciente.
    **conjuntos : dict[str, list/array]
        Conjuntos difusos discretizados sobre x.
        Ej: baja=[...], media=[...], alta=[...]

    Retorna
    -------
    Clase instanciable con:
      - self.x
      - self.<conjunto>
      - self.mf_<conjunto>(v)
      - evaluar(varianza) -> (dominante, v, pertenencias, inferencia)
    """
    class FuzzyVarianzaTemplate:
        def __init__(self):
            self.x = np.array(x, dtype=float)

            # guardar vectores de cada conjunto
            for nombre, valores in conjuntos.items():
                setattr(self, nombre, np.array(valores, dtype=float))

            # crear interpoladores (membership functions)
            for nombre in conjuntos.keys():
                vector = getattr(self, nombre)
                setattr(
                    self,
                    f"mf_{nombre}",
                    lambda v, vec=vector: np.interp(v, self.x, vec, left=vec[0], right=vec[-1])
                )

        def evaluar(self, varianza):
            v = float(varianza)

            pertenencias = {}
            for nombre in conjuntos.keys():
                mf = getattr(self, f"mf_{nombre}")
                pertenencias[nombre] = float(np.clip(mf(v), 0, 1))

            dominante = max(pertenencias, key=pertenencias.get)
            inferencia = pertenencias[dominante]
            return dominante, v, pertenencias, inferencia

    FuzzyVarianzaTemplate.__name__ = nombre_clase
    return FuzzyVarianzaTemplate

