# =============================================================================
# BLOQUE — FAMILIAS DE ESPERA (COOLDOWN) POR VARIABLE / SP
#
# IDEA
#   - El cooldown se guarda por "FAMILIA".
#   - Por defecto, la familia = el SP afectado por la acción (ACCION_A_SP[accion]).
#   - Así, todas las acciones que modifican el mismo SP comparten espera:
#       aumentar/disminuir + suave/normal => MISMA familia.
#
# CONFIGURACIÓN SIMPLE
#   1) Ajusta COOLDOWN_FAMILIA_S (segundos por SP/familia).
#   2) (Opcional) Si alguna acción debe ir a otra familia, usa ACCION_COOLDOWN_FAMILIA.
#   3) (Opcional) Si quieres duraciones específicas por acción, usa COOLDOWN_ACCION_S.
#
# PRIORIDAD PARA RESOLVER DURACIÓN (segundos):
#   A) COOLDOWN_ACCION_S[accion]                 (si existe)
#   B) COOLDOWN_FAMILIA_S[familia]              (familia por SP, o override)
#   C) regla['duracion'] dict: accion o familia
#   D) regla['duracion'] numérica
#   E) 0
# =============================================================================

# 1) Duración por familia (recomendado: familia = nombre del SP)
COOLDOWN_FAMILIA_S = {
    "sp_tonelaje": 180,
    "sp_agua_molino": 60,
    "sp_agua_cajon": 60,
    "sp_agua_ciclones": 60,
    "sp_rpm_bomba": 30,
}

# 2) (Opcional) Overrides de familia por acción (si necesitas excepciones)
#    Ejemplo: si quieres que cierto ajuste de agua use otra familia distinta al SP.
ACCION_COOLDOWN_FAMILIA = {
    # "aumentar_agua_molino_suave": "sp_agua_molino",  # normalmente no hace falta: ya cae por SP
}

# 3) (Opcional) Duraciones específicas por acción (si necesitas excepciones finas)
COOLDOWN_ACCION_S = {
    # "aumentar_tonelaje": 240,  # ejemplo: sobreescribe familia
}

def _cooldown_family(accion: str) -> str:
    """
    Devuelve la familia de cooldown para una acción.
    Prioridad:
      1) override explícito ACCION_COOLDOWN_FAMILIA
      2) SP asociado en ACCION_A_SP (familia por variable controlada)
      3) fallback: la acción misma (último recurso)
    """
    a = str(accion)

    if a in ACCION_COOLDOWN_FAMILIA:
        return str(ACCION_COOLDOWN_FAMILIA[a])

    sp = ACCION_A_SP.get(a, None)
    if sp is not None:
        return str(sp)

    return a

def cooldown_segundos_para_accion(accion: str, regla: dict) -> float:
    """
    Resuelve la duración de cooldown (s) para una acción considerando familias.
    """
    a = str(accion)
    fam = _cooldown_family(a)

    # A) por acción específica
    if a in COOLDOWN_ACCION_S:
        try:
            return float(COOLDOWN_ACCION_S[a])
        except Exception:
            return 0.0

    # B) por familia
    if fam in COOLDOWN_FAMILIA_S:
        try:
            return float(COOLDOWN_FAMILIA_S[fam])
        except Exception:
            return 0.0

    # C/D) desde la regla (si lo quieres por regla)
    dur = (regla or {}).get("duracion", 0)

    if isinstance(dur, dict):
        # por acción
        if a in dur:
            try:
                return float(dur[a])
            except Exception:
                return 0.0
        # por familia
        if fam in dur:
            try:
                return float(dur[fam])
            except Exception:
                return 0.0
        return 0.0

    try:
        return float(dur) if dur is not None else 0.0
    except Exception:
        return 0.0

