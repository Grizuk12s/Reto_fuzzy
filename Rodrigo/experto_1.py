def _norm_cond_text(s: str) -> str:
    """
    Normaliza textos de condiciones sin romper nombres con '_' como CERCA_ALTO.
    Acepta también variantes con '-' al momento de buscar en el diccionario pert.
    """
    if s is None:
        return ""
    return str(s).strip().upper()


def _mu_label(pert: dict, label: str) -> float:
    """
    Busca membresía en pert admitiendo variantes:
      - label tal cual
      - reemplazo '-' <-> '_'
    """
    if pert is None:
        return 0.0

    lab = _norm_cond_text(label)
    if lab in pert:
        return float(pert[lab])

    lab2 = lab.replace("-", "_")
    if lab2 in pert:
        return float(pert[lab2])

    lab3 = lab.replace("_", "-")
    if lab3 in pert:
        return float(pert[lab3])

    return 0.0


def mu_condicion(signal_fuzzy: dict, condicion) -> float:
    """
    Calcula μ(condición) dada una señal fuzzificada:
      signal_fuzzy = {"pert": {...}, "dom": ..., "val": ..., ...}

    Soporta:
      - "ALTO", "OK", "BAJO", "SUBIENDO", etc.
      - "NO-XXX" o "NO_XXX" (negación): 1 - μ(XXX)
    """
    pert = (signal_fuzzy or {}).get("pert", {}) or {}

    cond = _norm_cond_text(condicion)

    # negación
    if cond.startswith("NO-") or cond.startswith("NO_"):
        base = cond[3:]  # quita NO- / NO_
        mu_base = _mu_label(pert, base)
        return float(np.clip(1.0 - mu_base, 0.0, 1.0))

    return float(np.clip(_mu_label(pert, cond), 0.0, 1.0))


def fuerza_regla(result: dict, regla: dict, tnorm: str = "min") -> float:
    """
    Calcula la fuerza w de una regla usando:
      - AND con min (tnorm="min")
      - soporta cláusulas OR: {"OR":[(var,cond),(var,cond),...]}

    w = min( μ(cláusula_i) ) donde una cláusula OR vale max(...)
    """
    clauses = (regla or {}).get("si", []) or []
    if len(clauses) == 0:
        return 0.0

    mus = []

    for clause in clauses:
        # OR
        if isinstance(clause, dict) and "OR" in clause:
            m_or = 0.0
            for (var, cond) in clause["OR"]:
                if var not in result:
                    mu_v = 0.0
                else:
                    mu_v = mu_condicion(result[var], cond)
                m_or = max(m_or, float(mu_v))
            mus.append(float(m_or))
            continue

        # (var, cond)
        var, cond = clause
        if var not in result:
            mus.append(0.0)
        else:
            mus.append(float(mu_condicion(result[var], cond)))

    if not mus:
        return 0.0

    # AND: min (consistente con tu implementación original)
    if str(tnorm).lower() == "min":
        return float(min(mus))

    # fallback: también min
    return float(min(mus))

